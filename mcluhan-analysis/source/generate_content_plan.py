# ── HELPER FUNCTIONS ──────────────────────────────────────────────────────────

def load_analysis_db():
    """Load the analysis database and return as dict keyed by spread number."""
    with open(DB_PATH, encoding="utf-8") as f:
        db = json.load(f)
    by_num = {}
    for entry in db["spreads"]:
        num = int(entry["id"].split("_")[1])
        by_num[num] = entry
    return by_num


def generate_page_plan(spread_num, plan_data, analysis_entry):
    """Generate a single page plan entry from SPREAD_PLANS + analysis data."""
    # Extract original analysis summary
    original = analysis_entry
    orig_themes = original.get("themes", {}).get("original_themes", [])
    orig_strategy = original.get("rhetoric", {}).get("rhetorical_strategy", {}).get("primary", "")
    orig_type = original.get("spread_type", "")
    orig_section = original.get("section", "")
    book_pages = original.get("book_pages", [])
    orig_text = original.get("text", {}).get("main_text_content", "")
    orig_design_desc = original.get("rhetoric", {}).get("design_argument_description", "")
    if isinstance(orig_design_desc, bool):
        orig_design_desc = ""

    # Build original_summary
    original_summary = {
        "spread_type": orig_type,
        "section": orig_section,
        "book_pages": book_pages,
        "original_themes": orig_themes,
        "rhetorical_strategy": orig_strategy,
        "design_enacts_argument": original.get("rhetoric", {}).get("design_enacts_argument", False),
        "design_argument_description": orig_design_desc,
        "text_excerpt": orig_text[:300] if orig_text else "",
    }

    # Build contemporary_plan from SPREAD_PLANS entry
    contemporary_plan = {
        "theme": plan_data["theme"],
        "argument": plan_data["argument"],
        "text": {
            "approach": plan_data["text_approach"],
            "description": plan_data["text_description"],
            "voice_notes": plan_data["voice_notes"],
        },
        "image": {
            "approach": plan_data["image_approach"],
            "subject": plan_data["image_subject"],
            "mood": plan_data["image_mood"],
            "image_text_relationship": plan_data["image_text_rel"],
        },
        "rhetoric": {
            "strategy": plan_data["strategy"],
            "design_enacts": plan_data["design_enacts"],
        },
        "mapping": {
            "relationship_to_original": plan_data["relationship"],
            "convergences": plan_data["convergences"],
            "thinkers": plan_data["thinkers"],
            "quotations": plan_data["quotes"],
        },
        "dependencies": plan_data["dependencies"],
    }

    # Build paper_trace (if present)
    paper_trace = None
    if plan_data.get("paper_trace"):
        paper_trace = {
            "citation": plan_data["paper_trace"],
            "convergence_tag": plan_data.get("paper_convergence"),
        }

    # Assemble page plan
    page_plan = {
        "spread_id": f"spread_{spread_num:03d}",
        "movement": MOVEMENT_ASSIGNMENTS[spread_num],
        "original_summary": original_summary,
        "contemporary_plan": contemporary_plan,
        "paper_trace": paper_trace,
        "reviewer_feedback": {
            "status": "pending",
            "notes": [],
            "revision_count": 0,
        },
    }
    return page_plan


def run_quality_checks(content_plan):
    """Run quality checks on the generated content plan. Returns list of warnings."""
    warnings = []
    pages = content_plan.get("pages", [])
    meta = content_plan.get("meta", {})

    # Check all 85 pages present
    if len(pages) != 85:
        warnings.append(f"CRITICAL: Expected 85 pages, got {len(pages)}")

    # Strategy distribution
    strategy_counts = Counter()
    for p in pages:
        strat = p.get("contemporary_plan", {}).get("rhetoric", {}).get("strategy", "")
        strategy_counts[strat] += 1
    dominant = strategy_counts.most_common(1)
    if dominant and dominant[0][1] > 25:
        warnings.append(
            f"WARNING: Strategy '{dominant[0][0]}' used {dominant[0][1]} times "
            f"(>{25}/85). Consider more variety."
        )
    unused = [s for s in STRATEGIES if strategy_counts.get(s, 0) == 0]
    if unused:
        warnings.append(f"INFO: Unused strategies: {unused}")

    # Text approach distribution
    text_counts = Counter()
    for p in pages:
        approach = p.get("contemporary_plan", {}).get("text", {}).get("approach", "")
        text_counts[approach] += 1
    if text_counts.get("original_prose", 0) > 50:
        warnings.append(
            f"WARNING: 'original_prose' text approach used {text_counts['original_prose']} times. "
            "Consider more quotation and combination spreads."
        )

    # Image approach distribution
    img_counts = Counter()
    for p in pages:
        approach = p.get("contemporary_plan", {}).get("image", {}).get("approach", "")
        img_counts[approach] += 1

    # Convergence coverage
    convergence_hits = Counter()
    for p in pages:
        for c in p.get("contemporary_plan", {}).get("mapping", {}).get("convergences", []):
            convergence_hits[c] += 1
    for cid in CONVERGENCE_IDS:
        if convergence_hits.get(cid, 0) == 0:
            warnings.append(f"WARNING: Convergence '{cid}' has no page assignments")
        elif convergence_hits.get(cid, 0) < 3:
            warnings.append(
                f"INFO: Convergence '{cid}' only assigned to {convergence_hits[cid]} pages"
            )

    # Relationship distribution
    rel_counts = Counter()
    for p in pages:
        rel = p.get("contemporary_plan", {}).get("mapping", {}).get("relationship_to_original", "")
        rel_counts[rel] += 1
    for r in RELATIONSHIPS:
        if rel_counts.get(r, 0) == 0:
            warnings.append(f"WARNING: Relationship type '{r}' never used")

    # Paper trace count
    paper_count = sum(1 for p in pages if p.get("paper_trace") is not None)
    if paper_count < 10:
        warnings.append(f"INFO: Only {paper_count} pages have paper_trace (target: 12-15)")
    elif paper_count > 20:
        warnings.append(f"INFO: {paper_count} pages have paper_trace (target: 12-15, may be too many)")

    # Thinker distribution
    thinker_counts = Counter()
    for p in pages:
        for t in p.get("contemporary_plan", {}).get("mapping", {}).get("thinkers", []):
            thinker_counts[t] += 1
    if thinker_counts:
        top = thinker_counts.most_common(3)
        warnings.append(f"INFO: Top thinkers: {[(t, c) for t, c in top]}")

    # Movement distribution
    mov_counts = Counter()
    for p in pages:
        mov_counts[p.get("movement", "")] += 1
    expected = {
        "prologue": 10, "movement_1_environment": 25,
        "movement_2_acceleration": 25, "hinge": 5,
        "movement_3_dreamscape": 20,
    }
    for mov, exp in expected.items():
        actual = mov_counts.get(mov, 0)
        if actual != exp:
            warnings.append(f"WARNING: Movement '{mov}' has {actual} pages (expected {exp})")

    # Quotation count
    total_quotes = sum(
        len(p.get("contemporary_plan", {}).get("mapping", {}).get("quotations", []))
        for p in pages
    )
    warnings.append(f"INFO: Total quotations assigned: {total_quotes}")

    return warnings


def main():
    """Generate content_plan.json."""
    import argparse
    parser = argparse.ArgumentParser(description="Generate content plan for companion book")
    parser.add_argument("--meta-only", action="store_true",
                        help="Generate meta section only (Phase B)")
    parser.add_argument("--dry-run", action="store_true",
                        help="Validate without writing output file")
    args = parser.parse_args()

    print("=" * 60)
    print("Planning Engine: Generating content_plan.json")
    print("=" * 60)

    # Load analysis database
    print("\nLoading analysis database...")
    analysis_db = load_analysis_db()
    print(f"  Loaded {len(analysis_db)} spreads")

    # Generate meta section
    print("\nGenerating meta section...")
    meta = generate_meta()
    print(f"  Movements: {len(meta['movement_plan'])}")
    print(f"  Convergences: {len(meta['convergence_map'].get('convergences', []))}")

    if args.meta_only:
        content_plan = {
            "version": "1.0",
            "generated": datetime.now().isoformat(),
            "generator": "generate_content_plan.py",
            "schema_version": "1.2",
            "meta": meta,
            "pages": [],
        }
        if args.dry_run:
            print("\n[DRY RUN] Meta section generated. Would write to:")
            print(f"  {OUT_PATH}")
            print(json.dumps(meta, indent=2)[:500] + "...")
        else:
            with open(OUT_PATH, "w", encoding="utf-8") as f:
                json.dump(content_plan, f, indent=2, ensure_ascii=False)
            print(f"\n  Meta-only plan written to {OUT_PATH}")
        return

    # Generate page plans
    print("\nGenerating page plans...")
    pages = []
    missing = []
    for num in range(1, 86):
        if num not in SPREAD_PLANS:
            missing.append(num)
            continue
        if num not in analysis_db:
            missing.append(num)
            continue
        page_plan = generate_page_plan(num, SPREAD_PLANS[num], analysis_db[num])
        pages.append(page_plan)
    if missing:
        print(f"  WARNING: Missing data for spreads: {missing}")
    print(f"  Generated {len(pages)} page plans")

    # Assemble content plan
    content_plan = {
        "version": "1.0",
        "generated": datetime.now().isoformat(),
        "generator": "generate_content_plan.py",
        "schema_version": "1.2",
        "meta": meta,
        "pages": pages,
    }

    # Run quality checks
    print("\nRunning quality checks...")
    warnings = run_quality_checks(content_plan)
    for w in warnings:
        print(f"  {w}")

    if args.dry_run:
        print(f"\n[DRY RUN] Would write {len(pages)} pages to:")
        print(f"  {OUT_PATH}")
        print(f"\nFirst page preview:")
        if pages:
            print(json.dumps(pages[0], indent=2)[:600] + "...")
    else:
        with open(OUT_PATH, "w", encoding="utf-8") as f:
            json.dump(content_plan, f, indent=2, ensure_ascii=False)
        size_kb = os.path.getsize(OUT_PATH) / 1024
        print(f"\n  Content plan written to {OUT_PATH}")
        print(f"  File size: {size_kb:.1f} KB")
        print(f"  Pages: {len(pages)}")

    print("\n" + "=" * 60)
    print("Done.")
    print("=" * 60)


if __name__ == "__main__":
    main()
